#include <iostream>

using namespace std;

int main()
{
    cout << "operators in cpp:" << endl;
    cout << "following are the types of opereators in c++:" << endl;
    int a = 4,
        b = 5;
    cout << "the value of a +b is " << a + b << endl;
    cout << "the value of a -b is " << a - b << endl;
    cout << "the value of a /b is " << a / b << endl;
    cout << "the value of a++ is " << a++ << endl;
    cout << "the value of a-- is " << a-- << endl;
    cout << "the value of ++a  is " << ++a << endl;
    cout << "the value of --a is " << --a << endl;

    return 0;
}
